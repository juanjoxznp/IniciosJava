public class Ciclos {

    public static void main(String[] args) {

     ///   Ejemplo del ciclo "for" para imprimir los números del 1 al 5:

        public class CicloFor {
            public static void main(String[] args) {
                for (int i = 1; i <= 5; i++) {
                    System.out.println(i);
                }
            }
        }

      //  Ejemplo del ciclo "while" para imprimir los números del 1 al 5:
        public class CicloWhile {
            public static void main(String[] args) {
                int i = 1;
                while (i <= 5) {
                    System.out.println(i);
                    i++;
                }
            }
        }

     //   Ejemplo del ciclo "do-while" para imprimir los números del 1 al 5:
        public class CicloDoWhile {
            public static void main(String[] args) {
                int i = 1;
                do {
                    System.out.println(i);
                    i++;
                } while (i <= 5);
            }
        }


    }

}
