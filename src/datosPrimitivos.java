public class datosPrimitivos {

    public static void main(String[] args) {


        byte numeroByte = 10;
        short numeroShort = 2000;
        int numeroInt = 123456;
        long numeroLong = 1234567890L;

        System.out.println(numeroByte);  // Salida: 10
        System.out.println(numeroShort); // Salida: 2000
        System.out.println(numeroInt);   // Salida: 123456
        System.out.println(numeroLong);  // Salida: 1234567890

        float numeroFloat = 3.14f;
        double numeroDouble = 3.14159;

        System.out.println(numeroFloat);  // Salida: 3.14
        System.out.println(numeroDouble); // Salida: 3.14159

        char letra1 = 'A';
        char letra2 = 'B';
        char simbolo = '$';

        System.out.println(letra1);  // Salida: A
        System.out.println(letra2);  // Salida: B
        System.out.println(simbolo); // Salida: $

        boolean esVerdadero = true;
        boolean esFalso = false;

        System.out.println(esVerdadero); // Salida: true
        System.out.println(esFalso);




    }




}
